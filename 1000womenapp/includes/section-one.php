    <h2>“1000 WOMEN UNITED AGAINST DOMESTIC VIOLENCE & ABUSE INITIATIVE”</h2>
    <p>Back in June 2003, Wendy Ackerman joined WHEAT for a celebratory lunch at 
        Clovelly Golf Club as Raymond Ackerman funded a golf day for all the 
        friends of WHEAT Trust. We discussed the various ways we know of in which 
        communities have used to unite women and at the same time mobilise valuable funds 
        to invest in grassroots women’s initiatives. We all became very enthusiastic when 
        we learned more about Wendy Ackerman’s experiences when attending an event in the 
        USA where they sold 1000 tickets to women to attend a fundraising event. Our first 
        question was “Could we do it?” This great idea did not get lost in time – 
        Wendy invited me to meet the organiser of the USA event at a dinner at their 
        home and the idea became a PLAN.The next week I phoned a few members of the 
        EMINENT Women Network, a network that invested in WHEAT Trust, where, within one day, 
        we had a great working committee of 15 women; many of them with experience of domestic 
        violence – and the theme was decided on without much discussion. The timing was also 
        wonderful as the fist new legislation on domestic violence was passed by the South African parliament. 
        The working group remain true to the event and volunteer their service to organise the initiative.</p>
